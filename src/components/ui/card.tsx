import { cva, type VariantProps } from 'class-variance-authority';
import * as React from 'react';

import { cn } from '@/lib/utils';

const cardVariants = cva(
  'rounded-lg border bg-card text-card-foreground transition-all duration-200',
  {
    variants: {
      variant: {
        default: 'shadow-sm',
        elevated: 'shadow-lg hover:shadow-xl',
        outlined: 'border-2 shadow-none',
        ghost:
          'border-transparent bg-transparent shadow-none hover:bg-accent hover:text-accent-foreground',
        gradient: 'bg-gradient-to-br from-card to-card/50 shadow-md',
        interactive:
          'shadow-sm hover:shadow-md cursor-pointer hover:scale-[1.02] active:scale-[0.98]',
        error: 'border-destructive/50 bg-destructive/5 shadow-sm',
        success: 'border-green-500/50 bg-green-500/5 shadow-sm',
        warning: 'border-yellow-500/50 bg-yellow-500/5 shadow-sm',
      },
      size: {
        sm: 'p-3',
        md: 'p-4',
        lg: 'p-6',
      },
    },
    defaultVariants: {
      variant: 'default',
      size: 'md',
    },
  }
);

export interface CardProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof cardVariants> {}

const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant, size, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(cardVariants({ variant, size }), className)}
      {...props}
    />
  )
);
Card.displayName = 'Card';

